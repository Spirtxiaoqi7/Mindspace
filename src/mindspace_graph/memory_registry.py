class MemoryRegistry:
    def __init__(self, fields: tuple[MemoryField, ...] = FIELDS) -> None:
        self.fields = fields
        self._by_location = {(field.target, field.path): field for field in fields}
        self._by_code = {field.field_code: field for field in fields}
        if len(self._by_location) != len(fields) or len(self._by_code) != len(fields):
            raise ValueError("memory registry contains duplicate path or field_code")

    @staticmethod
    def base_path(path: str) -> str:
        parts = path.rstrip("/").split("/")
        if parts and (parts[-1] == "-" or parts[-1].isdigit()):
            return "/".join(parts[:-1]) or "/"
        return path

    def resolve(self, target: str, path: str) -> MemoryField | None:
        return self._by_location.get((target, self.base_path(path)))

    def by_code(self, field_code: str) -> MemoryField | None:
        return self._by_code.get(field_code)

    def public(self) -> list[dict[str, object]]:
        return [field.public() for field in self.fields]


DEFAULT_MEMORY_REGISTRY = MemoryRegistry()
