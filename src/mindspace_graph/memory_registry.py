"""Single source of truth for persistent profile fields and memory reduction."""

from __future__ import annotations

from dataclasses import asdict, dataclass
from typing import Literal

ValueKind = Literal["scalar", "list"]
ReducerKind = Literal["replace_one", "unique_set", "opposing_set", "bounded_event", "lifecycle"]
MemoryScope = Literal["user", "agent", "session"]
LifecycleKind = Literal["persistent", "temporal", "session"]


@dataclass(frozen=True, slots=True)
class MemoryField:
    field_code: str
    target: str
    path: str
    display_name: str
    category: str
    value_kind: ValueKind
    reducer: ReducerKind
    scope: MemoryScope
    lifecycle: LifecycleKind
    max_items: int = 1
    conflict_group: str = ""
    polarity: str = "neutral"

    def public(self) -> dict[str, object]:
        return asdict(self)


def _field(
    field_code: str,
    target: str,
    path: str,
    display_name: str,
    category: str,
    value_kind: ValueKind,
    reducer: ReducerKind,
    scope: MemoryScope,
    lifecycle: LifecycleKind,
    max_items: int = 1,
    conflict_group: str = "",
    polarity: str = "neutral",
) -> MemoryField:
    return MemoryField(
        field_code=field_code,
        target=target,
        path=path,
        display_name=display_name,
        category=category,
        value_kind=value_kind,
        reducer=reducer,
        scope=scope,
        lifecycle=lifecycle,
        max_items=max_items,
        conflict_group=conflict_group,
        polarity=polarity,
    )


